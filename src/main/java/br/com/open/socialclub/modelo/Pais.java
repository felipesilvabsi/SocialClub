package br.com.open.socialclub.modelo;

/**
 *
 * @author Felipe Silva
 */
public class Pais {
    
    long pais;
    String nome;
    String sigla;

    public long getPais() {
        return pais;
    }

    public void setPais(long pais) {
        this.pais = pais;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }
    
}
